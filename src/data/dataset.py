"""PyTorch Dataset for Google Speech Commands (12-class setup)."""
from __future__ import annotations

import random
from pathlib import Path
from typing import Callable, Optional

import torch
import torchaudio
from torch.utils.data import Dataset

from src.data.labels import (
    ALL_CLASSES,
    NUM_CLASSES,
    SILENCE_IDX,
    SILENCE_LABEL,
    TARGET_COMMANDS,
    flat_label,
)
from src.utils.constants import SAMPLE_RATE, CLIP_SAMPLES, SILENCE_FRACTION, N_SILENCE_FIXED
from src.utils.audio import load_audio

# Path suffix for the background noise folder inside the downloaded dataset
_BG_SUFFIX = "speech_commands_v0.01/_background_noise_"
_BG_SUFFIX_V2 = "speech_commands_v0.02/_background_noise_"


class SpeechCommandsDataset(Dataset):
    """Wraps torchaudio.datasets.SPEECHCOMMANDS with 12-class label mapping.

    Silence samples are generated by randomly cropping 1-second windows from
    the background noise audio files that come with the dataset.

    Args:
        root:              Directory where the dataset will be downloaded / is located.
        split:             One of "training", "validation", "testing".
        download:          Whether to download if not present.
        transform:         Callable applied to each waveform (e.g. feature extraction).
        silence_fraction:  Fraction of training samples to add as silence.
        n_silence_fixed:   Fixed number of silence samples for val/test splits.
        seed:              RNG seed for reproducible silence window selection.
    """

    def __init__(
        self,
        root: str | Path,
        split: str,
        download: bool = True,
        transform: Optional[Callable] = None,
        silence_fraction: float = SILENCE_FRACTION,
        n_silence_fixed: int = N_SILENCE_FIXED,
        seed: int = 42,
    ):
        assert split in ("training", "validation", "testing"), f"Unknown split: {split!r}"
        root = Path(root)
        self.transform = transform
        self.split = split

        self._speech_ds = torchaudio.datasets.SPEECHCOMMANDS(
            root=str(root), download=download, subset=split
        )

        # Pre-compute (raw) labels from file paths to avoid full audio loading
        self._speech_labels: list[int] = self._precompute_speech_labels()

        # Build silence clips from background noise files
        noise_dir = self._find_noise_dir(root)
        n_silence = (
            int(len(self._speech_ds) * silence_fraction)
            if split == "training"
            else n_silence_fixed
        )
        rng_seed = None if split == "training" else seed
        self._silence_clips: list[torch.Tensor] = _make_silence_clips(
            noise_dir, n_silence, rng_seed
        )

        # Combined label list (silence first, then speech)
        self._labels: list[int] = (
            [SILENCE_IDX] * len(self._silence_clips) + self._speech_labels
        )

    # ------------------------------------------------------------------
    # helpers
    # ------------------------------------------------------------------
    def _precompute_speech_labels(self) -> list[int]:
        """Extract labels from file paths without loading audio."""
        labels: list[int] = []
        for item in self._speech_ds._walker:
            # ``_walker`` may contain either relative paths ("yes/file.wav")
            # or absolute paths (".../SpeechCommands/.../yes/file.wav").
            # In both cases, the class word is the parent folder name.
            p = Path(item) if isinstance(item, str) else Path(item[0])
            word = p.parent.name
            labels.append(flat_label(word))
        return labels

    @staticmethod
    def _find_noise_dir(root: Path) -> Optional[Path]:
        for suffix in (_BG_SUFFIX, _BG_SUFFIX_V2):
            p = root / "SpeechCommands" / suffix
            if p.exists():
                return p
        # Fallback: recursive search
        for p in root.rglob("_background_noise_"):
            if p.is_dir():
                return p
        return None

    # ------------------------------------------------------------------
    # Dataset interface
    # ------------------------------------------------------------------
    def __len__(self) -> int:
        return len(self._silence_clips) + len(self._speech_ds)

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, int]:
        if idx < len(self._silence_clips):
            waveform = self._silence_clips[idx]
            label = SILENCE_IDX
        else:
            speech_idx = idx - len(self._silence_clips)
            wav_path = self._speech_ds._walker[speech_idx]
            waveform, sr = load_audio(wav_path)

            if sr != SAMPLE_RATE:
                waveform = torchaudio.functional.resample(waveform, sr, SAMPLE_RATE)
            label = self._speech_labels[speech_idx]

        waveform = _pad_or_trim(waveform, CLIP_SAMPLES)

        if self.transform is not None:
            waveform = self.transform(waveform)

        return waveform, label

    def get_labels(self) -> list[int]:
        """Return all labels without loading audio (for sampler construction)."""
        return list(self._labels)

    def class_counts(self) -> torch.Tensor:
        t = torch.tensor(self._labels, dtype=torch.long)
        return torch.bincount(t, minlength=NUM_CLASSES)


# ------------------------------------------------------------------
# Module-level helpers
# ------------------------------------------------------------------

def _pad_or_trim(wav: torch.Tensor, target: int) -> torch.Tensor:
    n = wav.shape[-1]
    if n == target:
        return wav
    if n > target:
        return wav[..., :target]
    return torch.nn.functional.pad(wav, (0, target - n))


def _make_silence_clips(
    noise_dir: Optional[Path],
    n: int,
    seed: Optional[int],
) -> list[torch.Tensor]:
    """Crop *n* random 1-second windows from background noise files."""
    if noise_dir is None or n == 0:
        return []

    noise_files = list(noise_dir.glob("*.wav"))
    if not noise_files:
        return []

    chunks: list[torch.Tensor] = []
    for nf in noise_files:
        try:
            wav, sr = load_audio(nf)
        except RuntimeError as e:
            # Handle backend issues (e.g., on macOS without libsndfile)
            import warnings
            warnings.warn(f"Failed to load {nf.name}: {e}. Skipping this noise file.")
            continue
        if wav.shape[0] > 1:
            wav = wav.mean(0, keepdim=True)  # stereo → mono
        if sr != SAMPLE_RATE:
            wav = torchaudio.functional.resample(wav, sr, SAMPLE_RATE)
        chunks.append(wav)

    # Handle case where no chunks could be loaded (backend issue)
    if not chunks:
        import warnings
        warnings.warn(
            f"No background noise files could be loaded from {noise_dir}. "
            "Silence samples will be generated as zeros. "
            "Install libsndfile (brew install libsndfile) to fix this."
        )
        # Generate silence as zero tensors
        clips: list[torch.Tensor] = []
        for _ in range(n):
            clips.append(torch.zeros(1, CLIP_SAMPLES))
        return clips

    pool = torch.cat(chunks, dim=-1)  # (1, total_samples)
    total = pool.shape[-1]

    rng = random.Random(seed)
    clips: list[torch.Tensor] = []
    for _ in range(n):
        if total >= CLIP_SAMPLES:
            start = rng.randint(0, total - CLIP_SAMPLES)
            clip = pool[..., start : start + CLIP_SAMPLES]
        else:
            clip = torch.nn.functional.pad(pool, (0, CLIP_SAMPLES - total))
        clips.append(clip.clone())
    return clips
